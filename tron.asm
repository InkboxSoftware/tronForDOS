;TIMER FUNCTIONS
TIMERHANDLER:
    PUSH AX
    INC WORD [CS:TIMERTICKS]
    
    ;CALL ORIGINAL HANDLER EVERY 3 TICKS
    INC BYTE [CS:CHAINCOUNT]
    CMP BYTE [CS:CHAINCOUNT], 3
    JB AFTERCHAIN
    MOV BYTE [CS:CHAINCOUNT], 0
    POP AX
    JMP FAR [CS:OLDINTOFFSET]

AFTERCHAIN:
    MOV AL, 20H ;END OF INT TO PIC
    OUT 20H, AL
    POP AX
    IRET

