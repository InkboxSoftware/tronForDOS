    ADD AL, 30H
    INT 10H
    MOV AL, [CAR1SCORE]
    ADD AL, 30H
    INT 10H
    ;SET CURSOR
    MOV AH, 02H
    MOV BH, 00H
    MOV DX, 0021H
    INT 10H
    ;OUTPUT TO SCREEN
    MOV AH, 0EH
    MOV AL, [CAR2SCORE+2]
