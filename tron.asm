    MOV BH, 00H
    MOV BL, [CAR2C]
    INT 10H
    MOV AL, [CAR2SCORE+1]
    ADD AL, 30H
    INT 10H
    MOV AL, [CAR2SCORE]
    ADD AL, 30H
    INT 10H

    RET

