GETCOLORSWAPINPUT:
    ;CHECK TURN TIMER
    MOV AL, [CAR1TURNTIMER]
    CMP AL, 00H
    JE CHECKCAR2DIRTIMER2
    DEC BYTE [CAR1TURNTIMER]
    
    CHECKCAR2DIRTIMER2:
    MOV AL, [CAR2TURNTIMER]
    CMP AL, 00H
    JE CHECKKEYS2
    DEC BYTE [CAR2TURNTIMER]


    CHECKKEYS2:
    ;CHECK FOR WASD KEYS
    MOV AH, 01H     ;CHECK FOR KEYSTROKE
    INT 16H         ;BIOS KEYBOARD SERVICE
    JZ NOMOREKEYS2  ;NOTHING FOUND
    MOV AH, 00H     ;GET KEY
    INT 16H
    CMP AL, 57H     ;W
    JE SWAPCAR1UP
    CMP AL, 53H     ;S
    JE SWAPCAR1DOWN
    CMP AL, 49H     ;I
    JE SWAPCAR2UP
    CMP AL, 4BH     ;K
    JE SWAPCAR2DOWN
    CMP AL, 20H     ;SPACE
    JE STARTUPGAME
    CMP AL, 1BH     ;ESC
    JE ESCAPEPRG
    JMP ENDKEYCHECK


    SWAPCAR1UP:
        MOV AL, [CAR1TURNTIMER]
        CMP AL, 00H
        JNE ENDKEYCHECK
        MOV AL, [CAR1CINDEX]
        INC AL
        AND AL, 0FH
        JMP SETCOLORCAR1
    SWAPCAR1DOWN:
        MOV AL, [CAR1TURNTIMER]
        CMP AL, 00H
        JNE ENDKEYCHECK
        MOV AL, [CAR1CINDEX]
        DEC AL
        AND AL, 0FH
    SETCOLORCAR1:
        MOV [CAR1CINDEX], AL
        MOV BYTE [CAR1TURNTIMER], 04H
        MOV BX, COLOR1INDEX
        XOR AH, AH
        ADD BX, AX
        MOV AL, [BX]
        MOV [CAR1C], AL
        ADD BX, 010H
        MOV AL, [BX]
        MOV [CAR1C2], AL
        CALL REDRAWTITLE
        JMP ENDKEYCHECK
    
    SWAPCAR2UP:
        MOV AL, [CAR2TURNTIMER]
        CMP AL, 00H
        JNE ENDKEYCHECK
        MOV AL, [CAR2CINDEX]
        INC AL
        AND AL, 0FH
        JMP SETCOLORCAR2
    SWAPCAR2DOWN:
        MOV AL, [CAR2TURNTIMER]
        CMP AL, 00H
        JNE ENDKEYCHECK
        MOV AL, [CAR2CINDEX]
        DEC AL
        AND AL, 0FH
    SETCOLORCAR2:
        MOV [CAR2CINDEX], AL
        MOV BYTE [CAR2TURNTIMER], 04H
        MOV BX, COLOR1INDEX
        XOR AH, AH
        ADD BX, AX
        MOV AL, [BX]
        MOV [CAR2C], AL
        ADD BX, 010H
        MOV AL, [BX]
        MOV [CAR2C2], AL
        CALL REDRAWTITLE
        JMP ENDKEYCHECK

    STARTUPGAME:
        XOR AL, AL
        MOV [ENDGAME], AL
        MOV [TITLEON], AL
        INC BYTE [RESETON]
            

    ENDKEYCHECK:
    JMP CHECKKEYS2

    NOMOREKEYS2:
    RET

DRAWRECT:   ;AL = COLOR, CHECKS RX, RY, RW, RH
    PUSH ES
    MOV BX, 0A000H
    MOV ES, BX
    ;CALCULATE INITIAL ADDRESS
    PUSH AX
    MOV AL, [RY]
    MOV BX, 140H
    MUL BX
    ADD AX, [RX]
    MOV DI, AX
    ;SET HEIGHT
    MOV BX, [RH]
    POP AX
    DRAWRECTLOOP:
        MOV CX, [RW]
    RSIDELOOP:    
        MOV [ES:DI], AL
        INC DI
        LOOP RSIDELOOP
        MOV DX, 140H
        SUB DX, [RW]
        ADD DI, DX
        DEC BX
        JNZ DRAWRECTLOOP
    POP ES
    RET

