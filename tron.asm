CARDIRECTIONCHANGE:
    ;CHECK TURN TIMER
    MOV AL, [CAR1TURNTIMER]
    CMP AL, 00H
    JE CHECKCAR2DIRTIMER
    DEC BYTE [CAR1TURNTIMER]
    
    CHECKCAR2DIRTIMER:
    MOV AL, [CAR2TURNTIMER]
    CMP AL, 00H
    JE CHECKKEYS
    DEC BYTE [CAR2TURNTIMER]


    CHECKKEYS:
    ;CHECK FOR WASD KEYS
    MOV AH, 01H     ;CHECK FOR KEYSTROKE
    INT 16H         ;BIOS KEYBOARD SERVICE
    JZ NOMOREKEYS   ;NOTHING FOUND
    MOV AH, 00H     ;GET KEY
    INT 16H
    CMP AL, 57H     ;W
    JE CAR1TURNUP
    CMP AL, 41H     ;A
    JE CAR1TURNLEFT
    CMP AL, 53H     ;S
    JE CAR1TURNDOWN
    CMP AL, 44H     ;D
    JE CAR1TURNRIGHT
    CMP AL, 49H     ;I
    JE CAR2TURNUP
    CMP AL, 4AH     ;J
    JE CAR2TURNLEFT
    CMP AL, 4BH     ;K
    JE CAR2TURNDOWN
    CMP AL, 4CH     ;L
    JE CAR2TURNRIGHT
    JMP ENDCARDIRCHANGE

    CAR1TURNUP: ;ENSURE GOING PROPER DIRECTION
        MOV AL, [CAR1TURNTIMER]
        CMP AL, 00H
        JNE ENDCARDIRCHANGE
        MOV AL, [CAR1DIR]
        CMP AL, 01H
        JA ENDCARDIRCHANGE     
        MOV AL, [CARW]
        MOV [CAR1TURNTIMER], AL   ;RESET TURN TIMER
        MOV BYTE [CAR1DIR], 03H
        JMP ENDCARDIRCHANGE
    CAR1TURNLEFT: ;ENSURE GOING PROPER DIRECTION
