    ENDCARDIRCHANGE:
        JMP CHECKKEYS

    NOMOREKEYS:
    XOR AX, AX
    RET




CHECKCOLLIDECAR1:
    MOV AX, [CAR1X]
    MOV [CHECKX], AX
    MOV AX, [CAR1Y]
    MOV [CHECKY], AX
    MOV AL, [CAR1DIR]
    MOV [CHECKDIR], AL
    CALL COLLIDECARCHECK
    ;RETURNS 00 IN AL IF ALL CLEAR
    MOV [CAR1DEAD], AL
    RET

CHECKCOLLIDECAR2:
    MOV AX, [CAR2X]
    MOV [CHECKX], AX
    MOV AX, [CAR2Y]
    MOV [CHECKY], AX
    MOV AL, [CAR2DIR]
    MOV [CHECKDIR], AL
    CALL COLLIDECARCHECK
    ;RETURNS 00 IN AL IF ALL CLEAR
    MOV [CAR2DEAD], AL
    RET

COLLIDECARCHECK:
    PUSH ES
    PUSH DI
    MOV BX, 0A000H
    MOV ES, BX

    ;CALCULATE POSITION TO CHECK
    MOV AX, [CHECKY]
    MOV BX, 320
    MUL BX
    ADD AX, [CHECKX]
    MOV DI, AX
   
    ;CHECK DIRECTION
    MOV AL, [CHECKDIR]
    CMP AL, 00H
    JE CHECKSIDERIGHT
    CMP AL, 01H
    JE CHECKSIDELEFT
    CMP AL, 02H
    JE CHECKSIDEBOTTOM
    JMP CHECKSIDETOP

    CHECKSIDERIGHT:
        MOV CX,[CARH]
        ADD DI,04H      ;GET NEW POSITION
        XOR AX, AX
        SIDECHECKLOOP:
            MOV BL, [ES:DI]
            OR AL, BL
            ADD DI, 320
            LOOP SIDECHECKLOOP
        JMP ENDCOLLIDECARCHECK  
    CHECKSIDELEFT:
        MOV CX,[CARH]
        DEC DI          ;GET NEW POSITION
        XOR AX, AX
        SIDECHECKLOOP2:
            MOV BL, [ES:DI]
            OR AL, BL
            ADD DI, 320
            LOOP SIDECHECKLOOP2
        JMP ENDCOLLIDECARCHECK
    CHECKSIDEBOTTOM:
        MOV CX,[CARH]
        ADD DI,1280     ;GET NEW POSITION
        XOR AX, AX
        SIDECHECKLOOP3:
            MOV BL, [ES:DI]
            OR AL, BL
            INC DI
            LOOP SIDECHECKLOOP3
        JMP ENDCOLLIDECARCHECK
    CHECKSIDETOP:
        MOV CX,[CARH]
        SUB DI,320      ;GET NEW POSITION
        XOR AX, AX
        SIDECHECKLOOP4:
            MOV BL, [ES:DI]
            OR AL, BL
            INC DI
            LOOP SIDECHECKLOOP4
       

    ENDCOLLIDECARCHECK:
    POP DI
    POP ES
    RET

INCREASESCORE:
    ;CHECK IF CAR1 DIED
    MOV AL, [CAR1DEAD]
    CMP AL, 00H
    JNE CHECKCAR2DEAD
        ;INCREASE SCORE
        MOV AL, [CAR1SCORE]
        ADD AL, 01H
        AAA ;ASCII ADJUST AFTER ADDITION
        MOV [CAR1SCORE], AL
        MOV AL, AH
        XOR AH, AH
        ADD AL, [CAR1SCORE+1]
        AAA
        MOV [CAR1SCORE+1], AL
        MOV AL, AH
        XOR AH, AH
        ADD AL, [CAR1SCORE+2]
        AAA
        MOV [CAR1SCORE+2], AL    

    CHECKCAR2DEAD:
    ;CHECK IF CAR2 DIED
    MOV AL, [CAR2DEAD]
    CMP AL, 00H
    JNE DRAWSCORE
        ;INCREASE SCORE
        MOV AL, [CAR2SCORE]
        ADD AL, 01H
        AAA ;ASCII ADJUST AFTER ADDITION
        MOV [CAR2SCORE], AL
        MOV AL, AH
        XOR AH, AH
        ADD AL, [CAR2SCORE+1]
        AAA
        MOV [CAR2SCORE+1], AL
        MOV AL, AH
        XOR AH, AH
        ADD AL, [CAR2SCORE+2]
        AAA
        MOV [CAR2SCORE+2], AL

DRAWSCORE:
    ENDINCREASESCORE:
