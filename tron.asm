ORG 100H

SECTION .TEXT
    COLOR1 EQU 36H

MAIN:
    ;SAVE OLD INTERRUPT
    MOV AX, 3508H   ;GET INTERRUPT VECTOR FOR INT 08H
    INT 21H
    MOV [CS:OLDINTOFFSET], BX
    MOV [CS:OLDINTSEGMENT], ES

    ;SET NEW PIT COUNT
    CLI
    MOV AL, 36H ;TIMER 0, MODE 3
    OUT 43H, AL
    MOV AX, 4DB6H   ;DIVISOR TO GET 60HZ
    OUT 40H, AL     ;LSB
    MOV AL, AH
    OUT 40H, AL     ;MSB
  
    ;SET NEW INTERRUPT HANDLER
    MOV DX, TIMERHANDLER
    MOV AX, 2508H
    INT 21H
    STI

    ;INITIALIZE PROGRAM
    CALL SETGRAPHICSMODE
SETTITLE:
    CALL DRAWTITLE
    JMP TIMELOOP

RESETGAME:
    CALL SETBORDERS
    CALL RESETCARS
    CALL DRAWCARS

TIMELOOP:
    MOV AX, [CS:TIMERTICKS]
    CMP AX, [LASTTICK]
    JE TIMELOOP

    MOV [LASTTICK], AX

    ;ONE TICK HAS PASSED
    MOV AL, [ENDGAME]
    CMP AL, 00H
    JNE AFTERMOVECARS
        CALL MOVECARS
    AFTERMOVECARS:

    MOV AL, [TITLEON]
    CMP AL, 00H
    JE AFTERTITLE
        CALL RUNTITLE
    AFTERTITLE:
    MOV AL, [RESETON]
    CMP AL, 00H
    JE AFTERWARDS
        MOV BYTE [RESETON], 00H
        JMP RESETGAME
    AFTERWARDS:

    
    ;CHECK FOR ESC KEY
    MOV AH, 01H     ;CHECK FOR KEYSTROKE
    INT 16H         ;BIOS KEYBOARD SERVICE
    JZ TIMELOOP     ;NOTHING FOUND
    MOV AH, 00H     ;GET KEY
    INT 16H
    CMP AL, 20H     ;SPACE - RESET
    JE RESETGAME
    CMP AL, 1BH     ;ESC
    JNE TIMELOOP

ESCAPEPRG:

RESTORETIMER:
    CLI     ;DISABLE INTERRUPTS
    ;SET ORIGINAL FREQUENCY
    MOV AL, 36H
    OUT 43H, AL
    XOR AX, AX
    OUT 40H, AL
    OUT 40H, AL
    ;RESTORE ORIGINAL INTERUPT
    PUSH DS
    MOV DX, [OLDINTOFFSET]
    MOV DS, [OLDINTSEGMENT]
    MOV AX, 2508H
    INT 21H

    MOV AL, 20H
    OUT 20H, AL

    STI     ;ENABLE INTERRUPTS

    POP DS
    
    CALL CLEANUPGRAPHICS
    LEA DX, [MESSAGEDONE]
    MOV AH, 09H
    INT 21H
EXIT:
    MOV AX, 4C00H
    INT 21H

;GAME FUNCTIONS
MOVECARS:
  ;CHECK KEYBOARD INPUT
    CALL CARDIRECTIONCHANGE

  GOCAR1:
  ;CHECK CAR1 TIMER
    INC WORD [CAR1TIMER]
    MOV AL, [CAR1TIMER]
    CMP AL, [CAR1S] ;SPEED
    JB GOCAR2
        ;RESET TIMER
        XOR AX, AX
        MOV [CAR1TIMER], AX
  ;DRAW TRAIL
    MOV AL, [CAR1C2]
    CALL DRAWCAR1
  ;MOVE IN DIRECTION
    CALL MOVECAR1
  ;DRAWCAR1
    MOV AL, [CAR1C]
    CALL DRAWCAR1

  GOCAR2:
  ;CHECK CAR2 TIMER
    INC WORD [CAR2TIMER]
    MOV AL, [CAR2TIMER]
    CMP AL, [CAR2S] ;SPEED
    JB CHECKDEADCAR
        ;RESET TIMER
        XOR AX, AX
        MOV [CAR2TIMER], AX
  ;DRAW TRAIL
    MOV AL, [CAR2C2]
    CALL DRAWCAR2
  ;MOVE IN DIRECTION
    CALL MOVECAR2
  ;DRAWCAR2
    MOV AL, [CAR2C]
    CALL DRAWCAR2
    CALL CHECKCOLLIDECAR2   ;I KNOW THIS IS DOUBLE CALLED
        ;IT NEEDS TO BE DOUBLE CALLED OR IT DOESN'T DETECT CAR ON CAR COLLIDE

  CHECKDEADCAR:
    CALL CHECKCOLLIDECAR1
    MOV AL, [CAR1DEAD]
    CMP AL, 00H
    JE CHECKDEADCAR2
    MOV AL, 01H
    MOV [ENDGAME], AL
    MOV AL, 0FH
    CALL DRAWCAR1
    CALL INCREASESCORE
    

  CHECKDEADCAR2:
    CALL CHECKCOLLIDECAR2
    MOV AL, [CAR2DEAD]
    CMP AL, 00H
    JE ENDMOVECARS
    MOV AL, 01H
    MOV [ENDGAME], AL
    MOV AL, 0FH
    CALL DRAWCAR2
    CALL INCREASESCORE
    

  ENDMOVECARS:  
    RET

MOVECAR1:
    ;CHECKDIRECTION
    MOV AL, [CAR1DIR]
    CMP AL, 00H
    JE MOVECAR1RIGHT
    CMP AL, 01H
    JE MOVECAR1LEFT
    CMP AL, 02H
    JE MOVECAR1DOWN
    JMP MOVECAR1UP

    MOVECAR1RIGHT:
        INC WORD [CAR1X]
        JMP ENDMOVECAR1
    MOVECAR1LEFT:
        DEC WORD [CAR1X]
        JMP ENDMOVECAR1
    MOVECAR1DOWN:
        INC WORD [CAR1Y]
        JMP ENDMOVECAR1
    MOVECAR1UP:
        DEC WORD [CAR1Y]
    
    ENDMOVECAR1:
    RET

